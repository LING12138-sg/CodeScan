export function getIntlLocale(locale) {
  return locale === 'en' ? 'en-US' : 'zh-CN'
}

export function getMessage(locale, key, params = {}) {
  const dictionary = messages[locale] || messages[DEFAULT_LOCALE]
  const fallback = messages[DEFAULT_LOCALE]
  const value = resolveMessage(dictionary, key) ?? resolveMessage(fallback, key) ?? key
  if (typeof value !== 'string') {
    return key
  }
  return value.replace(/\{(\w+)\}/g, (_, name) => {
    const param = params[name]
    return param === undefined || param === null ? '' : String(param)
  })
}

function resolveMessage(source, key) {
  return key.split('.').reduce((current, part) => {
    if (current && typeof current === 'object' && part in current) {
      return current[part]
    }
    return undefined
  }, source)
}
